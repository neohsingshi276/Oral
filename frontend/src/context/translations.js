const flattenTextPairs = (from, to, pairs = {}) => {
  if (typeof from === 'string' && typeof to === 'string') {
    pairs[from] = to;
    return pairs;
  }
  if (Array.isArray(from) && Array.isArray(to)) {
    from.forEach((item, index) => flattenTextPairs(item, to[index], pairs));
    return pairs;
  }
  if (from && to && typeof from === 'object' && typeof to === 'object') {
    Object.keys(from).forEach(key => flattenTextPairs(from[key], to[key], pairs));
  }
  return pairs;
};

